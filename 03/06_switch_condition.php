<?php 

/*
	SWITCH
	=================================================
	Eksekusi dilakukan jika CASE cocok dengan kondisi
*/


$i = 0;
switch ($i) {
	case '0':
		echo 'i sama dengan nol';
		break;

	case '1':
		echo 'i sama dengan satu';
		break;	
	
	default:
		echo 'i bukan nol ataupun satu';
		break;
}


 ?>